//   fill-blank       — sentence with ___, pick from word-bank options
//   word-order       — tap words in the right order (options = word tiles, answer = full sentence)
//   odd-one-out      — 4 words, pick the one that doesn't belong
//   a-an             — choose 'a' or 'an' before a word

